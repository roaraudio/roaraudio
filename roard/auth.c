#include "roard.h"

#define _NONE ROAR_AUTH_T_AUTO

int auth_init (void) {
 int i;

 memset(g_auth_keyring, 0, sizeof(g_auth_keyring));

 for (i = 0; i < AUTH_KEYRING_LEN; i++) {
  g_auth_keyring[i].type = _NONE;
 }

 return 0;
}

int auth_free (void) {
 return 0;
}

